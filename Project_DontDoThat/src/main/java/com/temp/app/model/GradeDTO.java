package com.temp.app.model;

public class GradeDTO {
	int num; 
	String accommodation;
	int review;
	double clean; 
	double comfortable;
	double location;
	double facilities; 
	double kind;
	double value; 
	double wifi;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getAccommodation() {
		return accommodation;
	}
	public void setAccommodation(String accommodation) {
		this.accommodation = accommodation;
	}
	public int getReview() {
		return review;
	}
	public void setReview(int review) {
		this.review = review;
	}
	public double getClean() {
		return clean;
	}
	public void setClean(double clean) {
		this.clean = clean;
	}
	public double getComfortable() {
		return comfortable;
	}
	public void setComfortable(double comfortable) {
		this.comfortable = comfortable;
	}
	public double getLocation() {
		return location;
	}
	public void setLocation(double location) {
		this.location = location;
	}
	public double getFacilities() {
		return facilities;
	}
	public void setFacilities(double facilities) {
		this.facilities = facilities;
	}
	public double getKind() {
		return kind;
	}
	public void setKind(double kind) {
		this.kind = kind;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	public double getWifi() {
		return wifi;
	}
	public void setWifi(double wifi) {
		this.wifi = wifi;
	}
}
